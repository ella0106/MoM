class LlavaMetaModel:

    def __init__(self, config):
        super(LlavaMetaModel, self).__init__(config)
        
        self.vision_tower = build_vision_tower()
        config.mm_hidden_size = self.vision_tower.hidden_size
        self.vision_tower.config.mm_projector_type = config.mm_projector_type
        self.mm_projector = build_vision_projector(config, vision_cfg=self.vision_tower.config)        
        self.motion_tower = build_motion_tower(config)
        self.fusion_module = build_fusion_module(config)
        
